function get_idols_key_title(){

  let key, title, filtered_idols;
  while (true) {
    key = $('input[name=choose_data]:checked').val();
    if (key == 'all_data'){
      //ランダムに選択する
      key =  datas[Math.floor(Math.random() * (datas.length - 1))][1];
    }
  
    title = $('input[name=game_title]:checked').val();
  
    filtered_idols = filter_by_title(filter_by_key(idols, key), title);

    if (filtered_idols.length > 6) break;
  }
  return [filtered_idols, key, title];



}

function refresh_list(){
  //回答状況をリセット
  answered = false;
  $(function () {$("#btn-next").css({"visibility": "hidden",});});
  const nth = 6;
  const [filtered_idols, key, title] = get_idols_key_title();
  const reverse = -1;

  chosen_key = key;

  //並べ替え
  filtered_idols.sort(function (a, b) {
    let ret = 1;
    if (b[key] > a[key]){
      ret = 1;
    } else if (b[key] < a[key]) {
      ret = -1;
    } else {
      ret = 0;
    }
    if (reverse) {
      ret = ret*-1;
    }
    return ret;
  });

  //適当に出題する場所を探す
  const num = getRandomInt(0,filtered_idols.length - nth - 1);
  //例題に出す5人分
  const samples = filtered_idols.slice(num, num+nth-1);
  //正解の6人目
  const correct = filtered_idols[num+nth];
  correct['correct'] = true;
  //候補者リスト
  let candidates = [];
  candidates.push(correct);
  //候補者除外リスト
  let except_indexes = [];
  for (let i = num; i < num + nth; i++){
    except_indexes.push(i);
  }

  //候補者数
  const num_candidates = 5;
  for (let i = 0; i < num_candidates - 1; i++){
    while(true){
      //console.log(Math.random());
      //候補者を適当に探す
      let index_candidate = getRandomInt(0,filtered_idols.length-1);
      //候補者除外リストにnumがすでに入っていたらもう一度やりなおし
      if (except_indexes.indexOf(index_candidate) > -1){
        continue;
      }
      except_indexes.push(index_candidate);
      //候補者のデータ取得
      let candidate = filtered_idols[index_candidate];
      //候補者のデータが正解と同じだったらもう一度やり直し
      if (candidate[key] == correct[key]){
        continue;
      }
      //5人までのデータが全部同じで、6人目が次のデータになった場合は、5人までのデータと同じアイドルも正解とみなすべきなので候補者からはずす。
      if (candidate[key] == samples[0]){
        continue;
      }
      candidate['correct'] = false;

      //問題なければ候補者リストに追加
      candidates.push(candidate);
      break;
    }
  }


  //5人目までを表示
  let sample_html = '';
  sample_html +=  `<h2>さぁいらっしゃい</h2>`
  sample_html += `<ul class="list-group">`;
  for (let i = 0; i < samples.length; i++){
    const sample = samples[i];
    sample_html += `<li class="list-group-item text-dark">${i+1}人目は${sample['label']} <span class="data">(${data_str(sample[key],key)})</span></li>`;
  }
  sample_html += `</ul>`;
  document.getElementById("samples").innerHTML = sample_html;

  //6人目の候補を表示
  const shuffled_candidates = shuffle(candidates);
  let candidates_html = '';
  candidates_html += `<h2>6人目はだぁれ?</h2>`;
  /*
  candidates_html += `<ul class="list-group">`;
  for (let i = 0; i < candidates.length; i++){
    const candidate = shuffled_candidates[i];
    const correct = candidate['correct'] ? '〇' : '×'
    candidates_html += `
      <li class="list-group-item" onclick="on_answered(${candidate['correct']})">
      <button type="button" class="btn btn-outline-primary">
      ${candidate['label']} ? 
      <span class="data">(${data_str(candidate[key],key)})</span>
      <span class="correct">${correct}</span>
      </button>
      </li>
      `;
  }
  candidates_html += `</ul>`;
  */

  candidates_html += `<div class="btn-group-vertical" role="group" aria-label="Candidates list">`;
  for (let i = 0; i < candidates.length; i++) {
    const candidate = shuffled_candidates[i];
    const correct = candidate['correct'] ? '〇' : '×';
    const button_id = 'candidate' + i;
    candidates_html += `
    <button type="button" class="btn btn-outline-primary text-left btn-candidate" onclick="on_answered(${candidate['correct']}, \'${button_id}\')" id="${button_id}">
      ${candidate['label']} ? 
      <span class="data">(${data_str(candidate[key],key)})</span>
      <span class="correct">${correct}</span>
      </button>
      `;
  }
  candidates_html += `</div>`;

  document.getElementById("candidates").innerHTML = candidates_html;



 
  //console.log(samples);
  //console.log(shuffled_candidates);
}



