//選択肢を選んだ
function on_answered(correct, button_id) {
  if (answered) return;
  answered = true;
  
  $(function () {
    //選択肢ボタンを無効化(固定化)
    if (correct){
      $("#"+button_id).removeClass("btn-outline-primary").addClass("btn-success");
    } else {
      $("#"+button_id).removeClass("btn-outline-primary").addClass("btn-danger");
    }
    
    //隠していた〇×を表示
    $(".data, .correct").css({
      "visibility": "visible",
    });
    //次へ移動するボタンを表示
    $("#btn-next").css({
      "visibility": "visible",
    });

    //ランダムを選択していた場合、どのキーが選ばれていたのかを表示
    const key = $('input[name=choose_data]:checked').val();
    if (key == 'all_data'){
      let key_label = '';
      for (let i = 0; i < datas.length; i++){
        if (datas[i][1] == chosen_key){
          key_label = datas[i][0];
        }
      }
      document.getElementById('chosen_key').innerHTML = `並び順：${key_label} `;
    }

  });

  //得点表示
  update_score(correct);

  
}

//得点表示
function update_score(ansewer){
  let total = score.total;
  let correct = score.correct;

  total += 1;
  correct += ansewer ? 1 : 0;

  score.total = total;
  score.correct = correct;

  show_score();
  save_score();
}

function show_score(){
  const total = score.total;
  const correct = score.correct;

  const rate = total ? Math.floor(correct/total*100) : 0;

  document.getElementById('stats').innerHTML = `
  <h2>秘密のお話</h2>
  <p>${rate}% (${correct}/${total})</p>`;
}

function save_score(){
  localStorage.setItem('whos6th_score', JSON.stringify(score));
}

