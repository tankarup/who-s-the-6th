// 配列をシャッフル
function shuffle(array) {
  for (let i = array.length - 1; i >= 0; i--) {
    let rand = Math.floor(Math.random() * (i + 1));
    // 配列の数値を入れ替える
    [array[i], array[rand]] = [array[rand], array[i]]
  }
  return array;
}

//アイドルを指定した事務所でフィルタリング。リストになければ全事務所が対象
function filter_by_title(idols, title){
  let title_keys = [];
  for (let i = 0; i < titles.length - 1; i++){
    title_keys.push(titles[i][1]);
  }
  if (title_keys.indexOf(title) > -1){
    return idols.filter(idol => idol['title'] == title);
  } else {
    return idols;
  }
  
}

function filter_by_key(idols, key){
  return idols.filter(idol => idol[key]);
}
//https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/random
function getRandomInt(mino, maxo) {
  const min = Math.ceil(mino);
  const max = Math.floor(maxo);
  const r = Math.floor(Math.random() * (max - min + 1)) + min;
  //console.log(r, min, max);

  return r; //The maximum is inclusive and the minimum is inclusive 
}

function data_str(data, category){
  switch (category) {
    case 'birthday':

      let month = data.substring(2,4);
      month = month[0] == '0' ? month[1] : month;
      month = month + '月';
      let day = data.substring(5,7);
      day = day[0] == '0' ? day[1] : day;
      day = day + '日';
      return month + day;//あとで修正
      break;
    case 'bust':
    case 'waist':
    case 'hip':
    case 'height':
    case 'shoe':
      return data + 'cm';
      break;
    case 'age':
      return data + '歳';
      break;
    case 'weight':
      return data + 'kg';
      break;
  }
}

